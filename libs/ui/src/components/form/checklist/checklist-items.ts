export interface CheckListItems {
    /**
     * The value of the option when selected.
     */
    value: string;
    /**
     * The displayed text of the drop down item.
     */
    text: string;
    /**
     * If a badge is displayed.
     */
    badge?: boolean;
    /**
     * Number associated to the badge if one.
     */
    badgeNumber?: number;

}
